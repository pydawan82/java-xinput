
public class XInputException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1537138382668157550L;

	public XInputException() {
		super();
	}

	public XInputException(String message, Throwable cause) {
		super(message, cause);
	}

	public XInputException(String message) {
		super(message);
	}

	public XInputException(Throwable cause) {
		super(cause);
	}

}
